function add(a, b) {
  const result = a + b;
  console.log(`${a} + ${b} = ${result}`);
}

// ? Použití const function
const subtract = (a, b) => {
  const result = a - b;
  console.log(`${a} - ${b} = ${result}`);
};

function multiple(a, b) {
  const result = a * b;
  console.log(`${a} * ${b} = ${result}`);
}

function divide(a, b) {
  const result = a / b;
  console.log(`${a} / ${b} = ${result}`);
}

add(15, 48);
subtract(98, 41);
multiple(54, 30);
divide(88, 6);
